CREATE TABLE users (
	user_id int(11) AUTO INCREMENT PRIMARY KEY not NULL,
	Org_name varchar(256) not null,
	Department varchar(256) not null,
	Ministry varchar(256) not null,
	Contact varchar(256) not null,
	Designation varchar(256) not null,
	Mobile varchar(256) not null,
	Confirm varchar(256) not null,
	Email varchar(256) not null,
	Start_date varchar(256) not null,
	Last_date varchar(256) not null,
	Age_limit varchar(256) not null,
	No_of_post varchar(256) not null
);

